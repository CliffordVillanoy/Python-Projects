fruit_basket = ['Apple', 'Kiwi', 'Orange', 'Mango'] # List of fruits
fruit_basket.sort() # Sort in ascending order
print(fruit_basket) # Output: ['Apple', 'Kiwi', 'Mango', 'Orange']
fruit_basket.sort(reverse=True) # Sort in descending order
print(fruit_basket) # Output: ['Orange', 'Mango', 'Kiwi', 'Apple']      